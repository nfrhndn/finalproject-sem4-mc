create extension if not exists btree_gist;

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  email text not null,
  phone text,
  gender text check (gender in ('male', 'female')),
  photo_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.cities (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  photo_url text,
  created_at timestamptz not null default now()
);

create table public.court_categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  created_at timestamptz not null default now()
);

create table public.courts (
  id bigint generated by default as identity primary key,
  city_id bigint not null references public.cities(id) on delete restrict,
  category_id bigint references public.court_categories(id) on delete set null,
  name text not null,
  thumbnail_url text,
  about text,
  material text not null,
  price_per_hour integer not null check (price_per_hour >= 0),
  address text not null,
  phone text,
  status text not null default 'active' check (status in ('active', 'inactive', 'maintenance')),
  is_featured boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.court_images (
  id bigint generated by default as identity primary key,
  court_id bigint not null references public.courts(id) on delete cascade,
  image_url text not null,
  sort_order integer not null default 0
);

create table public.court_features (
  id bigint generated by default as identity primary key,
  court_id bigint not null references public.courts(id) on delete cascade,
  name text not null
);

create table public.court_operating_hours (
  id bigint generated by default as identity primary key,
  court_id bigint not null references public.courts(id) on delete cascade,
  day_of_week integer not null check (day_of_week between 0 and 6),
  open_hour integer not null check (open_hour between 0 and 23),
  close_hour integer not null check (close_hour between 1 and 24),
  unique (court_id, day_of_week),
  check (open_hour < close_hour)
);

create table public.court_blocked_slots (
  id bigint generated by default as identity primary key,
  court_id bigint not null references public.courts(id) on delete cascade,
  blocked_date date not null,
  start_hour integer not null check (start_hour between 0 and 23),
  end_hour integer not null check (end_hour between 1 and 24),
  reason text,
  check (start_hour < end_hour)
);

create table public.bookings (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  court_id bigint not null references public.courts(id) on delete restrict,
  booking_date date not null,
  start_hour integer not null check (start_hour between 0 and 23),
  end_hour integer not null check (end_hour between 1 and 24),
  total_hours integer generated always as (end_hour - start_hour) stored,
  price_per_hour integer not null check (price_per_hour >= 0),
  sub_total integer generated always as ((end_hour - start_hour) * price_per_hour) stored,
  tax_amount integer not null default 0 check (tax_amount >= 0),
  grand_total integer generated always as (((end_hour - start_hour) * price_per_hour) + tax_amount) stored,
  status text not null default 'pending_payment' check (status in ('pending_payment', 'paid', 'cancelled', 'expired', 'failed')),
  expires_at timestamptz,
  time_range int4range generated always as (int4range(start_hour, end_hour, '[)')) stored,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (start_hour < end_hour),
  exclude using gist (
    court_id with =,
    booking_date with =,
    time_range with &&
  ) where (status in ('pending_payment', 'paid'))
);

create table public.payments (
  id bigint generated by default as identity primary key,
  booking_id bigint not null references public.bookings(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  provider text not null default 'midtrans',
  provider_order_id text unique,
  snap_token text,
  redirect_url text,
  amount integer not null check (amount >= 0),
  status text not null default 'pending' check (status in ('pending', 'settlement', 'capture', 'deny', 'cancel', 'expire', 'failure')),
  raw_payload jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);


create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, name, email, phone, gender, photo_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'name', split_part(new.email, '@', 1)),
    new.email,
    new.raw_user_meta_data->>'phone',
    nullif(new.raw_user_meta_data->>'gender', ''),
    new.raw_user_meta_data->>'photo_url'
  )
  on conflict (id) do update set
    name = excluded.name,
    email = excluded.email,
    phone = excluded.phone,
    gender = excluded.gender,
    photo_url = coalesce(excluded.photo_url, public.profiles.photo_url),
    updated_at = now();

  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.format_rupiah(amount integer)
returns text
language sql
stable
as $$
  select 'Rp ' || replace(to_char(amount, 'FM999,999,999,999'), ',', '.');
$$;

create or replace function public.court_to_json(c public.courts)
returns jsonb
language sql
stable
as $$
  select jsonb_build_object(
    'id', c.id,
    'name', c.name,
    'thumbnail', c.thumbnail_url,
    'photos', coalesce((select jsonb_agg(ci.image_url order by ci.sort_order, ci.id) from public.court_images ci where ci.court_id = c.id), '[]'::jsonb),
    'about', c.about,
    'features', coalesce((select jsonb_agg(cf.name order by cf.id) from public.court_features cf where cf.court_id = c.id), '[]'::jsonb),
    'material', c.material,
    'price_per_hour', c.price_per_hour,
    'price_per_hour_formatted', public.format_rupiah(c.price_per_hour),
    'address', c.address,
    'phone', c.phone,
    'status', c.status,
    'bookings_this_month', (
      select count(*)::integer
      from public.bookings b
      where b.court_id = c.id
        and b.status in ('pending_payment', 'paid')
        and date_trunc('month', b.booking_date::timestamp) = date_trunc('month', now())
    ),
    'city', (select jsonb_build_object('id', ct.id, 'name', ct.name) from public.cities ct where ct.id = c.city_id),
    'category', (select jsonb_build_object('id', cc.id, 'name', cc.name) from public.court_categories cc where cc.id = c.category_id)
  );
$$;

create or replace function public.booking_to_json(b public.bookings)
returns jsonb
language sql
stable
as $$
  select jsonb_build_object(
    'id', b.id,
    'date', b.booking_date::text,
    'date_formatted', to_char(b.booking_date, 'FMDay, DD Mon YYYY'),
    'start_time', lpad(b.start_hour::text, 2, '0') || ':00',
    'end_time', lpad(b.end_hour::text, 2, '0') || ':00',
    'time_slot', lpad(b.start_hour::text, 2, '0') || ':00 - ' || lpad(b.end_hour::text, 2, '0') || ':00',
    'total_hours', b.total_hours,
    'price_per_hour', b.price_per_hour,
    'sub_total', b.sub_total,
    'tax_amount', b.tax_amount,
    'grand_total', b.grand_total,
    'grand_total_formatted', public.format_rupiah(b.grand_total),
    'status', b.status,
    'court', (
      select jsonb_build_object(
        'id', c.id,
        'name', c.name,
        'thumbnail', c.thumbnail_url,
        'material', c.material,
        'address', c.address,
        'phone', c.phone,
        'city', (select jsonb_build_object('id', ct.id, 'name', ct.name) from public.cities ct where ct.id = c.city_id),
        'category', (select jsonb_build_object('id', cc.id, 'name', cc.name) from public.court_categories cc where cc.id = c.category_id)
      )
      from public.courts c
      where c.id = b.court_id
    ),
    'created_at', b.created_at::text
  );
$$;

create or replace function public.get_available_slots(p_court_id bigint, p_date date)
returns jsonb
language plpgsql
stable
as $$
declare
  court_record public.courts;
  hours_record public.court_operating_hours;
  slots jsonb;
begin
  select * into court_record from public.courts where id = p_court_id and status = 'active';
  if not found then
    raise exception 'Court is not available';
  end if;

  select * into hours_record
  from public.court_operating_hours
  where court_id = p_court_id
    and day_of_week = extract(dow from p_date)::integer;

  if not found then
    return jsonb_build_object(
      'court_id', court_record.id,
      'court_name', court_record.name,
      'date', p_date::text,
      'price_per_hour', court_record.price_per_hour,
      'slots', '[]'::jsonb
    );
  end if;

  select jsonb_agg(
    jsonb_build_object(
      'time', lpad(hour_value::text, 2, '0') || ':00',
      'available', not exists (
        select 1
        from public.bookings b
        where b.court_id = p_court_id
          and b.booking_date = p_date
          and b.status in ('pending_payment', 'paid')
          and int4range(b.start_hour, b.end_hour, '[)') && int4range(hour_value, hour_value + 1, '[)')
      ) and not exists (
        select 1
        from public.court_blocked_slots bs
        where bs.court_id = p_court_id
          and bs.blocked_date = p_date
          and int4range(bs.start_hour, bs.end_hour, '[)') && int4range(hour_value, hour_value + 1, '[)')
      )
    )
    order by hour_value
  ) into slots
  from generate_series(hours_record.open_hour, hours_record.close_hour - 1) as hour_value;

  return jsonb_build_object(
    'court_id', court_record.id,
    'court_name', court_record.name,
    'date', p_date::text,
    'price_per_hour', court_record.price_per_hour,
    'slots', coalesce(slots, '[]'::jsonb)
  );
end;
$$;

create or replace function public.create_booking(
  p_court_id bigint,
  p_date date,
  p_start_hour integer,
  p_end_hour integer
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  court_record public.courts;
  booking_record public.bookings;
  expires_at_value timestamptz := now() + interval '15 minutes';
begin
  if current_user_id is null then
    raise exception 'Not authenticated';
  end if;

  if p_start_hour >= p_end_hour then
    raise exception 'Invalid time range';
  end if;

  select * into court_record from public.courts where id = p_court_id and status = 'active';
  if not found then
    raise exception 'Court is not available';
  end if;

  insert into public.bookings (
    user_id,
    court_id,
    booking_date,
    start_hour,
    end_hour,
    price_per_hour,
    tax_amount,
    status,
    expires_at
  )
  values (
    current_user_id,
    p_court_id,
    p_date,
    p_start_hour,
    p_end_hour,
    court_record.price_per_hour,
    0,
    'pending_payment',
    expires_at_value
  )
  returning * into booking_record;

  return jsonb_build_object(
    'booking', public.booking_to_json(booking_record),
    'expires_at', expires_at_value::text,
    'expires_in_seconds', 900
  );
exception
  when exclusion_violation then
    raise exception 'Selected time slot is no longer available';
end;
$$;

